<h1 class="font-medium text-4xl">TailwindCSS</h1>
Siden er opbygget med TailwindCSS, som er et opensource CSS Utility framework. TailwindCSS er en meget tilpasselig CSS framework på lavt niveau, der giver dig alle de byggesten, du har brug for til at bygge skræddersyede designs uden irriterende meningsfulde stilarter, du skal kæmpe for at tilsidesætte.
Da de fleste CSS framework har <strong >for</strong> meget. De kommer med alle mulige foruddefinerede komponenter som knapper, kort og alerts, der måske hjælper dig med at bevæge dig hurtigt i starten, men forårsager mere skade, end de gør gavn, når det er tid til at få dit websted til at skille sig ud med et brugerdefineret design.
Det er her TailwindCSS er anderledes.
<br><br>
I stedet for meningsfulde foruddesignede komponenter giver Tailwind værktøjsklasser på lavt niveau, der giver dig mulighed for at oprette helt tilpassede designs uden nogensinde at forlade din HTML.
Hvert Tailwind-værktøj leveres også med responsive varianter, hvilket gør det ekstremt nemt at opbygge responsive grænseflader uden at ty til brugerdefineret CSS.
Mens du kan gøre meget med bare hjælpeklasser, kan det, når et projekt vokser, være nyttigt at kodificere almindelige mønstre til abstraktioner på højere niveau.
Hvis det giver mening at være tilpasselig, giver Tailwind dig mulighed for at tilpasse det. Dette inkluderer farver, kantstørrelser, fontvægte, afstandsværktøjer, breakpoints, skygger og masser mere.

Tailwind er skrevet i PostCSS og konfigureret i JavaScript, hvilket betyder, at du har den fulde kraft af et ægte programmeringssprog lige ved hånden.

Tailwind er mere end en CSS-framework, det er en motor til at skabe designsystemer.

