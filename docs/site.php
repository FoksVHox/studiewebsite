<h1 class="font-medium text-4xl">PHP</h1>
PHP er et objekt-orienteret server-side programmeringssprog anvendt til udvikling af dynamiske webapplikationer og websteder. Sproget blev grundlagt af Rasmus Lerdorf og udgivet første gang den 8. juni 1995.
PHP er et populært script til almene formål, som er særligt velegnet til webudvikling. Hurtig, fleksibel og pragmatisk, PHP styrer alt fra din blog til de mest populære websteder i verden.
